package biz.isphere.core.swt.widgets;

public class ContentAssistProposal {

    private String value;
    private String label;

    public ContentAssistProposal(String value) {
        this(value, null);
    }

    public ContentAssistProposal(String value, String label) {
        this.value = value;
        this.label = label;
    }

    public String getValue() {
        return value;
    }

    public String getLabel() {
        return label;
    }

}
